fn main() {
    // Addition
    let sum: u32 = 5 + 10;
    println!("The value of sum is {sum}");

    // Subtraction
    let sum: i32 = 5 - 10;
    println!("The value of sum is {sum}");

    // Multiplication
    let sum: u32 = 5 * 10;
    println!("The value of sum is {sum}");

    // Division
    let sum = 5.0 / 10.0;
    println!("The value of sum is {sum}");
}
